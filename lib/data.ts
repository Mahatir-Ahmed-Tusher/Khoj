// Helper function to get latest articles
export function getLatestArticles(count: number = 9): FactCheckArticle[] {
  return factCheckArticles
    .sort((a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime())
    .slice(0, count)
}

// Helper function to find related articles based on query
export function findRelatedArticles(query: string, maxResults: number = 3): FactCheckArticle[] {
  const searchTerms = query.toLowerCase().split(' ').filter(term => term.length > 2)
  
  const scoredArticles = factCheckArticles.map(article => {
    let score = 0
    
    // Check title match
    const titleLower = article.title.toLowerCase()
    searchTerms.forEach(term => {
      if (titleLower.includes(term)) score += 3
    })
    
    // Check claim match
    const claimLower = article.claim.toLowerCase()
    searchTerms.forEach(term => {
      if (claimLower.includes(term)) score += 2
    })
    
    // Check tags match
    article.tags.forEach(tag => {
      const tagLower = tag.toLowerCase()
      searchTerms.forEach(term => {
        if (tagLower.includes(term)) score += 1
      })
    })
    
    // Check content match (first 500 characters)
    const contentLower = article.content.substring(0, 500).toLowerCase()
    searchTerms.forEach(term => {
      if (contentLower.includes(term)) score += 0.5
    })
    
    return { ...article, relevanceScore: score }
  })
  
  // Filter articles with at least some relevance and sort by score
  return scoredArticles
    .filter(article => article.relevanceScore > 0)
    .sort((a, b) => b.relevanceScore - a.relevanceScore)
    .slice(0, maxResults)
    .map(({ relevanceScore, ...article }) => article) // Remove score from final result
}
